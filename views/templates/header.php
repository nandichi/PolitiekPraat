        case 'newsletter':
            if (isset($pathSegments[1])) {
                if ($pathSegments[1] === 'unsubscribe-success') {
                    $currentPage = 'newsletter-unsubscribe-success';
                } elseif ($pathSegments[1] === 'unsubscribe-error') {
                    $currentPage = 'newsletter-unsubscribe-error';
