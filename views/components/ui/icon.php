    ];

    /**
     * Render a Lucide icon as inline SVG.
     *
     * @param string                 $name  Icon name (zie $LUCIDE_PATHS keys)
     * @param int|array<string,mixed> $opts  Size (int) of options-array
     * @return string                Inline SVG markup
     */
    function pp_icon(string $name, $opts = []): string
    {
        if (is_int($opts)) {
            $opts = ['size' => $opts];
        }
        $size = (int) ($opts['size'] ?? 20);
        $class = (string) ($opts['class'] ?? '');
        $stroke = (string) ($opts['stroke'] ?? '1.75');
        $aria_label = $opts['label'] ?? null;
        $hidden = $aria_label === null;
        $path = $GLOBALS['LUCIDE_PATHS'][$name] ?? null;
        if ($path === null) {
            // Fallback: render een placeholder cirkel zodat layout niet breekt
            $path = '<circle cx="12" cy="12" r="10"/>';
        }

        $attrs = sprintf(
            'width="%d" height="%d" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="%s" stroke-linecap="round" stroke-linejoin="round"',
            $size,
            $size,
            htmlspecialchars($stroke, ENT_QUOTES, 'UTF-8')
        );
        if ($class !== '') {
            $attrs .= ' class="' . htmlspecialchars($class, ENT_QUOTES, 'UTF-8') . '"';
        }
        if ($hidden) {
            $attrs .= ' aria-hidden="true" focusable="false"';
        } else {
            $attrs .= ' role="img" aria-label="' . htmlspecialchars((string) $aria_label, ENT_QUOTES, 'UTF-8') . '"';
        }

        return "<svg {$attrs}>{$path}</svg>";
    }

    /**
     * Echo helper rondom pp_icon.
     */
    function pp_icon_e(string $name, $opts = []): void
    {
        echo pp_icon($name, $opts);
    }
